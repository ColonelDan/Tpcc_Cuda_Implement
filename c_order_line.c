#define RECORD_MAX_SIZE 900000 
#define RECORD_MAX_LENGTH 1000

// define order_line 
// primary :W_ID
struct order_line
{
        long OL_O_ID;
        long OL_D_ID;
        long OL_W_ID;
        long OL_NUMBER;
        long OL_I_ID;
        long OL_SUPPLY_W_ID;
        long OL_DELIVERY_D;
        double OL_QUANTITY;
        double OL_AMOUNT;
};

static int get_order_line(struct order_line **p_order_line_arr){ //?????????????????????????????????
	char file_name[] = "c_order_line.csv";  //?????????????????????????????????
    FILE *fp;  
    fp = fopen(file_name, "r");  
      
    if (!fp) {  
        fprintf(stderr, "failed to open file for reading\n");  
        return -1;  
    }
    
    char c;
    int i=0;
    int index=0;
    
    char **record_arr;
    record_arr=malloc(RECORD_MAX_SIZE*sizeof(char *));
    record_arr[index]=malloc(RECORD_MAX_LENGTH*sizeof(char));
    
    while(1){
      c=fgetc(fp);
      if(c==EOF){
        break;
      }
      if(c=='\n'){
        record_arr[index][i++]='\0';
        index++;
        record_arr[index]=malloc(RECORD_MAX_LENGTH*sizeof(char));
        i=0;
      }else{
        if(c!='"'){
          record_arr[index][i++]=c;
        }
      }
    }

    *p_order_line_arr=(struct order_line *)malloc(index*sizeof(struct order_line));//?????????????????????????????????
    
    for(i=1;i<index;i++){
      //printf("%s\n",record_arr[i]);
      char *token;
      
      token=strtok(record_arr[i],",");
      (*p_order_line_arr)[i].OL_O_ID=atol(token);//?????????????????????????????????
      
      token=strtok(NULL, ",");
      (*p_order_line_arr)[i].OL_D_ID=atol(token);//?????????????????????????????????
      
      token=strtok(NULL, ",");
      (*p_order_line_arr)[i].OL_W_ID=atol(token);//?????????????????????????????????
	  
	    token=strtok(NULL, ",");
      (*p_order_line_arr)[i].OL_NUMBER=atol(token);//?????????????????????????????????
	  
	    token=strtok(NULL, ",");
      (*p_order_line_arr)[i].OL_I_ID=atol(token);//?????????????????????????????????
	  
	    token=strtok(NULL, ",");
      (*p_order_line_arr)[i].OL_SUPPLY_W_ID=atol(token);//?????????????????????????????????

      token=strtok(NULL, ",");
      (*p_order_line_arr)[i].OL_DELIVERY_D=atol(token);//?????????????????????????????????
	  
	    token=strtok(NULL, ",");
      (*p_order_line_arr)[i].OL_QUANTITY=atof(token);//?????????????????????????????????
	  
      token=strtok(NULL, ",");
      (*p_order_line_arr)[i].OL_AMOUNT=atof(token);//?????????????????????????????????

      
      
      token=strtok(NULL, ",");
      
      //printf("!!!!!!!!!!!!!!!!\n%ld\n%s\n%s\n%s\n%s\n%s\n%s\n%lf\n%lf\n",order_line_arr[i].W_ID,order_line_arr[i].W_NAME,
	  //		order_line_arr[i].W_STREET_1,order_line_arr[i].W_STREET_2,order_line_arr[i].W_CITY,order_line_arr[i].W_STATE,
	  //		order_line_arr[i].W_ZIP,order_line_arr[i].W_TAX,order_line_arr[i].W_YTD);
    }
     
    fclose (fp);  
    
    return index;
}

