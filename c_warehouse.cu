#define RECORD_MAX_SIZE 10
#define RECORD_MAX_LENGTH 200


int get_warehouse(struct warehouse *p_warehouse_arr){ //?????????????????????????????????
	char file_name[] = "./csv/c_warehouse.csv";  //?????????????????????????????????
    FILE *fp;  
    fp = fopen(file_name, "r");  
      
    if (!fp) {  
        fprintf(stderr, "failed to open file for reading\n");  
        return -1;  
    }
    
    char c;
    int i=0;
    int index=0;
    
    char **record_arr;
    record_arr=(char **)malloc(RECORD_MAX_SIZE*sizeof(char *));
    record_arr[index]=(char *)malloc(RECORD_MAX_LENGTH*sizeof(char));
    
    while(1){
      c=fgetc(fp);
      if(c==EOF){
        break;
      }
      if(c=='\n'){
        record_arr[index][i++]='\0';
        index++;
        record_arr[index]=(char *)malloc(RECORD_MAX_LENGTH*sizeof(char));
        i=0;
      }else{
        if(c!='"'){
          record_arr[index][i++]=c;
        }
      }
    }

    //*p_warehouse_arr=(struct warehouse *)malloc((index-1)*sizeof(struct warehouse));//?????????????????????????????????
    
    for(i=1;i<index;i++){
      //printf("%s\n",record_arr[i]);
      char *token;
      
      token=strtok(record_arr[i],",");
      (p_warehouse_arr)[i-1].W_ID=atol(token);//?????????????????????????????????
      
      token=strtok(NULL, ",");
      strcpy((p_warehouse_arr)[i-1].W_NAME,token);//?????????????????????????????????
      
      token=strtok(NULL, ",");
      strcpy((p_warehouse_arr)[i-1].W_STREET_1,token);//?????????????????????????????????
	  
	  token=strtok(NULL, ",");
      strcpy((p_warehouse_arr)[i-1].W_STREET_2,token);//?????????????????????????????????
	  
	  token=strtok(NULL, ",");
      strcpy((p_warehouse_arr)[i-1].W_CITY,token);//?????????????????????????????????
	  
	  token=strtok(NULL, ",");
      strcpy((p_warehouse_arr)[i-1].W_STATE,token);//?????????????????????????????????
	  
	  token=strtok(NULL, ",");
      strcpy((p_warehouse_arr)[i-1].W_ZIP,token);//?????????????????????????????????
	  
	  token=strtok(NULL, ",");
      (p_warehouse_arr)[i-1].W_TAX=atof(token);//?????????????????????????????????
	  
	  token=strtok(NULL, ",");
      (p_warehouse_arr)[i-1].W_YTD=atof(token);//?????????????????????????????????
      
      token=strtok(NULL, ",");
      
      //printf("!!!!!!!!!!!!!!!!\n%ld\n%s\n%s\n%s\n%s\n%s\n%s\n%lf\n%lf\n",warehouse_arr[i].W_ID,warehouse_arr[i].W_NAME,
	  //		warehouse_arr[i].W_STREET_1,warehouse_arr[i].W_STREET_2,warehouse_arr[i].W_CITY,warehouse_arr[i].W_STATE,
	  //		warehouse_arr[i].W_ZIP,warehouse_arr[i].W_TAX,warehouse_arr[i].W_YTD);
      free(record_arr[i-1]);
    }
    free(record_arr);
    fclose (fp);  
    
    return index-1;
}
