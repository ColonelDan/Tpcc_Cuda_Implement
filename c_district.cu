#include <stdio.h>  
#include <string.h>  
#include <stdlib.h>

#define DISTRICT_RECORD_MAX_SIZE 100
#define DISTRICT_RECORD_MAX_LENGTH 200

#include "tpcc_table.h"
int get_district(struct district *p_district_arr){ //?????????????????????????????????
	char file_name[] = "./csv/c_district.csv";  //?????????????????????????????????
    FILE *fp;  
    fp = fopen(file_name, "r");  
      
    if (!fp) {  
        fprintf(stderr, "failed to open file for reading\n");  
        return -1;  
    }
    
    char c;
    int i=0;
    int index=0;
    
    char **record_arr;
    record_arr=(char **)malloc(DISTRICT_RECORD_MAX_SIZE*sizeof(char *));
    record_arr[index]=(char *)malloc(DISTRICT_RECORD_MAX_LENGTH*sizeof(char));
    
    while(1){
      c=fgetc(fp);
      if(c==EOF){
        break;
      }
      if(c=='\n'){
        record_arr[index][i++]='\0';
        index++;
        record_arr[index]=(char *)malloc(DISTRICT_RECORD_MAX_LENGTH*sizeof(char));
        i=0;
      }else{
        if(c!='"'){
          record_arr[index][i++]=c;
        }
      }
    }

    //*p_district_arr=(struct district *)malloc((index-1)*sizeof(struct district));//?????????????????????????????????
    
     
    
    for(i=1;i<index;i++){
      //printf("%s\n",record_arr[i]);
      char *token;
      
      token=strtok(record_arr[i],",");
      (p_district_arr)[i-1].D_ID=atol(token);//?????????????????????????????????
      
      token=strtok(NULL,",");
      (p_district_arr)[i-1].D_W_ID=atol(token);//?????????????????????????????????

      token=strtok(NULL, ",");
      strcpy((p_district_arr)[i-1].D_NAME,token);//?????????????????????????????????

      token=strtok(NULL, ",");
      strcpy((p_district_arr)[i-1].D_STREET_1,token);//?????????????????????????????????

	  token=strtok(NULL, ",");
      strcpy((p_district_arr)[i-1].D_STREET_2,token);//?????????????????????????????????
	  
	  token=strtok(NULL, ",");
      strcpy((p_district_arr)[i-1].D_CITY,token);//?????????????????????????????????
	  
	  token=strtok(NULL, ",");
      strcpy((p_district_arr)[i-1].D_STATE,token);//?????????????????????????????????
	  
	  token=strtok(NULL, ",");
      strcpy((p_district_arr)[i-1].D_ZIP,token);//?????????????????????????????????
	  
	  token=strtok(NULL, ",");
      (p_district_arr)[i-1].D_TAX=atof(token);//?????????????????????????????????
	  
	  token=strtok(NULL, ",");
      (p_district_arr)[i-1].D_YTD=atof(token);//?????????????????????????????????
      
      token=strtok(NULL,",");
      (p_district_arr)[i-1].D_NEXT_O_ID=atol(token);//?????????????????????????????????
      
      token=strtok(NULL,",");
	free(record_arr[i-1]);
    }
     free(record_arr);
    fclose (fp);  
    
    return index-1;
}
